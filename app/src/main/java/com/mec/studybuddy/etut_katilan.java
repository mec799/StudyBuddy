package com.mec.studybuddy;

public class etut_katilan {
    String id ;
    String nickk ;

    public etut_katilan() {
    }

    public etut_katilan(String id, String nickk) {
        this.id = id;
        this.nickk = nickk;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNickk() {
        return nickk;
    }

    public void setNickk(String nickk) {
        this.nickk = nickk;
    }
}
